void ADCConvertProcessTask(void *pvParameters)
{
	for (;;)
	{
		uint16_t data[2];
		if (xQueueReceive(q_ADCConvertProcessTask, &data, portMAX_DELAY) == pdTRUE)
		{
				CanTxMsgTypeDef tx;
				tx.Data[0] = (data[0] >> 8) & 0b00001111;
				tx.Data[1] = data[0];
				tx.StdId = 0x102;
				tx.DLC = 4;
				tx.RTR = CAN_RTR_DATA;
				tx.IDE = CAN_ID_STD;
				xQueueSendToBack(q_TXCANQueue, &tx, 100);

				tx.Data[0] = (data[1] >> 8) & 0b00001111;
				tx.Data[1] = data[1];
				tx.StdId = 0x103;
				tx.DLC = 2;
				tx.RTR = CAN_RTR_DATA;
				tx.IDE = CAN_ID_STD;
				xQueueSendToBack(q_TXCANQueue, &tx, 100);
		}
	}
}

long int error_count;
long int error;

void TXCANTask(void *pvParameters)
{
	for (;;)
	{
		//check if our task is triggered
		CanTxMsgTypeDef tx;
		if (xQueuePeek(q_TXCANQueue, &tx, portMAX_DELAY) == pdTRUE)
		{
			//check if CAN mutex is available
			if (xSemaphoreTake(m_CAN1, 50) == pdTRUE)
			{
				HAL_CAN_StateTypeDef state = HAL_CAN_GetState(&hcan1);
				if (state != HAL_CAN_STATE_ERROR)
				{
					xQueueReceive(q_TXCANQueue, &tx, portMAX_DELAY);
					hcan1.pTxMsg = &tx;
					HAL_CAN_Transmit_IT(&hcan1);
				}
				else if (state != HAL_CAN_STATE_ERROR)
				{
					error_count++;
					error = HAL_CAN_GetError(&hcan1);
				}
				//actually take item out of queue
				xSemaphoreGive(m_CAN1);  //release CAN mutex
			}

		}
	}
}
void RXCANTask(void *pvParameters)
{
	for (;;)
	{
		//check if CAN mutex is available
		if (xSemaphoreTake(m_CAN1, 10) == pdTRUE )//&& (hcan1.State == HAL_CAN_STATE_READY))
		{
			CanRxMsgTypeDef rx;

			hcan1.pRxMsg = &rx;
			HAL_CAN_StateTypeDef state = HAL_CAN_GetState(&hcan1);
			if (	state != HAL_CAN_STATE_ERROR ||
					state != HAL_CAN_STATE_BUSY_RX ||
					state != HAL_CAN_STATE_BUSY_TX_RX)
			{
				HAL_CAN_Receive_IT(&hcan1, 0);
			}
			else
			{
				error_count++;
				error = HAL_CAN_GetError(&hcan1);

			}
			xSemaphoreGive(m_CAN1);  //release CAN mutex
		}
		vTaskDelay(10);
	}
}

void RXCANProcessTask(void *pvParameters)
{
	for (;;)
	{
		CanRxMsgTypeDef rx;
		if (xQueueReceive(q_RXCANProcessQueue, &rx, portMAX_DELAY) == pdTRUE)
		{
			//process can message and send another can message
			CanTxMsgTypeDef tx;
			tx.Data[2] = rx.FMI;
			tx.Data[0] = rx.StdId >> 8;
			tx.Data[1] = rx.StdId;
			tx.Data[3] = 0;
			tx.Data[4] = rx.Data[0];
			tx.Data[5] = rx.Data[1];
			tx.Data[6] = rx.Data[2];
			tx.Data[7] = rx.Data[3];
			tx.StdId = rx.StdId - 1;
			tx.DLC = 8;
			tx.RTR = CAN_RTR_DATA;
			tx.IDE = CAN_ID_STD;
			xQueueSendToBack(q_TXCANQueue, &tx, 10);


		}
	}

}

