  /* add queues, ... */
  q_TXCANQueue = xQueueCreate(6, sizeof(CanTxMsgTypeDef));
  q_RXCANProcessQueue = xQueueCreate(5, sizeof(CanRxMsgTypeDef));
  q_ADCConvertProcessTask = xQueueCreate(3, 2*sizeof(uint16_t));

  /* USER CODE END RTOS_QUEUES */
 

  /* Start scheduler */
  osKernelStart();
  
  /* We should never get here as control is now taken by the scheduler */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

  }
